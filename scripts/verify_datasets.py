#!/usr/bin/env python3
"""Verify FGVC datasets: annotations exist and schemas are valid.

This repository uses two kinds of annotation sources:
1) `ref-data/`: canonical reference annotations (from the original authors).
2) `dataset/converted_from_data/`: annotations generated by `scripts/convert_data_to_json.py`.

This verifier checks the existence + basic schema of both, without requiring
all image files to exist on disk (image availability is environment-dependent).
"""

import json
import sys
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(PROJECT_ROOT))

from paths import PROJECT_ROOT, DATASET_ROOT, remap_path


def verify_naturalbench():
    print("\n--- naturalbench ---")
    p_ref = PROJECT_ROOT / "ref-data" / "naturalbench_ret_train.jsonl"
    p_conv = PROJECT_ROOT / "dataset" / "converted_from_data" / "naturalbench" / "train.jsonl"
    if not p_ref.exists():
        print(f"  FAIL: ref-data not found: {p_ref}")
        return False
    if not p_conv.exists():
        print(f"  WARN: converted naturalbench not found: {p_conv}")
    count = 0
    with open(p_ref) as f:
        for line in f:
            d = json.loads(line)
            img = remap_path(d["image"])
            assert "question" in d and "label" in d
            count += 1
            if count >= 3:
                break
    print(f"  Loaded {count} ref samples, schema OK (path remap callable)")
    return True


def verify_vlguard():
    print("\n--- vlguard ---")
    p_conv = PROJECT_ROOT / "dataset" / "converted_from_data" / "vlguard" / "train.json"
    if not p_conv.exists():
        print(f"  FAIL: converted vlguard not found: {p_conv}")
        return False
    with open(p_conv) as f:
        data = json.load(f)
    for d in data[:3]:
        img = remap_path(d["image"])
        assert "instruction" in d and "label" in d
    print(f"  Loaded {len(data)} samples, schema OK (path remap callable)")
    return True


def verify_mhalu():
    print("\n--- mhalu ---")
    p_ref = PROJECT_ROOT / "ref-data" / "mahalu_train.json"
    p_conv = PROJECT_ROOT / "dataset" / "converted_from_data" / "mhalubench" / "train.json"
    if not p_ref.exists():
        print(f"  FAIL: ref-data not found: {p_ref}")
        return False
    if not p_conv.exists():
        print(f"  WARN: converted mhalubench not found: {p_conv}")
    with open(p_ref) as f:
        data = json.load(f)
    for d in data[:3]:
        img = remap_path(d["image_path"])
        assert "claim" in d and "label" in d
    print(f"  Loaded {len(data)} ref samples, schema OK (path remap callable)")
    return True


def verify_blink():
    print("\n--- blink ---")
    p_ref = PROJECT_ROOT / "ref-data" / "blink_art_style_train.json"
    p_conv = PROJECT_ROOT / "dataset" / "converted_from_data" / "blink" / "art_style_val.json"
    if not p_ref.exists():
        print(f"  FAIL: ref-data not found: {p_ref}")
        return False
    if not p_conv.exists():
        print(f"  WARN: converted blink not found: {p_conv}")
    with open(p_ref) as f:
        data = json.load(f)
    for d in data[:3]:
        assert "question" in d and "label" in d and "image_1" in d
    print(f"  Loaded {len(data)} ref samples, schema OK")
    return True


def main():
    ok = sum([verify_naturalbench(), verify_vlguard(), verify_mhalu(), verify_blink()])
    print(f"\n{ok}/4 datasets verified")
    return 0 if ok == 4 else 1


if __name__ == "__main__":
    sys.exit(main())
